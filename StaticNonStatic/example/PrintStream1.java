class PrintStream1 {
    public void print(String str) {
        System.out.print(str);
    }

    public void print(int number) {
        System.out.print(number);
    }

    public void print(double number) {
        System.out.print(number);
    }

    public void print(boolean bool) {
        System.out.print(bool);
    }
}
